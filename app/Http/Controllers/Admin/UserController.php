    public function changeRole(ChangeRoleRequest $request, User $user)
    {
        $user->roles()->sync($request->role_ids);
        if (!$user->save()) {
            return redirect()->back()->with('error', 'Failed to change user role');
        }
        return redirect()->back()->with('success', 'User role changed successfully');
    }

